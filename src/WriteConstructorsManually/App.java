package WriteConstructorsManually;

public class App {

    String appName;
    String userName;
    String appPassword;
    String appCategory;

    public App(String appName, String userName, String appPassword, String appCategory) {
        this.appName = appName;
        this.userName = userName;
        this.appPassword = appPassword;
        this.appCategory = appCategory;
    }
}
