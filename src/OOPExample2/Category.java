package OOPExample2;

public class Category {

    private String cosmeticsCategory = "Cosmetics";

    private String meatCategory = "Meat";

    private String bakeryCategory = "Bakery";

    private String beverageCategory = "Beverage";

    private String seaFoodCategory = "SeaFood";


    public String getCosmeticsCategory() {
        return cosmeticsCategory;
    }

    public String getMeatCategory() {
        return meatCategory;
    }

    public String getBakeryCategory() {
        return bakeryCategory;
    }

    public String getBeverageCategory() {
        return beverageCategory;
    }

    public String getSeaFoodCategory() {
        return seaFoodCategory;
    }
}
